import boundaries from 'eslint-plugin-boundaries'

// ── Nuxt Layers boundary enforcement (eslint-plugin-boundaries@7) ─────────
// The layers/ shape (layers/<feature>, layers/shared) is only a REAL boundary
// because this rule fails `pnpm lint` on an illegal cross-layer import. Nuxt
// enforces nothing here — delete this and the architecture is just naming.
//
// PRECONDITION (ADR §5.1/5.3): each feature layer sets `imports: { scan: false }`
// in its own nuxt.config.ts. Without it, Nuxt auto-imports every layer's
// composables/utils globally, so a cross-layer use has no explicit `import`
// statement for this rule to inspect — the boundary would be invisible to lint.
//
// IMPORTANT (spike-confirmed false-negative trap, same as the Next pairing):
// `import/resolver.typescript` is load-bearing, not optional. Without
// eslint-import-resolver-typescript, the `~~/*` alias resolves to an *unknown*
// element, and unknown targets are silently PERMITTED — so real violations pass
// lint with no error. If a deliberately-broken cross-layer import ever passes
// `pnpm lint`, check this resolver first. (It reads the root tsconfig.json, which
// extends .nuxt/tsconfig.json — generated by `nuxt prepare` on postinstall — for
// the `~~`/`@` path mappings.)
//
// Exported and spread into eslint.config.mjs's withNuxt(...) by the scaffold.
// starter-only: the 8 cloned ui.nuxt.com templates are NOT restructured into
// Layers (see SKILL.md) so they don't ship this config.
export const boundariesConfig = {
  files: ['layers/**/*.{ts,vue}'],
  ignores: ['**/*.test.ts'],
  settings: {
    // Nuxt 4's root tsconfig.json is solution-style (`references` only, NO paths),
    // so `typescript: true` (which reads the root tsconfig) can't see the `~~`
    // alias — every cross-layer import then resolves to an *unknown* element and
    // is silently PERMITTED, defeating the whole rule. Point the resolver at the
    // generated per-environment tsconfigs (where `~~/* => ../*` actually lives) so
    // aliased imports resolve to real files the element patterns can classify.
    // These are generated by `nuxt prepare` (postinstall); if they're missing,
    // run `pnpm postinstall` before lint.
    'import/resolver': {
      typescript: {
        project: ['.nuxt/tsconfig.app.json', '.nuxt/tsconfig.server.json', '.nuxt/tsconfig.shared.json'],
        noWarnOnMultipleProjects: true
      }
    },
    // First match wins: `shared` is listed before the generic feature glob so
    // layers/shared/** classifies as shared, not as a feature named "shared".
    'boundaries/elements': [
      { type: 'shared', pattern: 'layers/shared/**' },
      { type: 'feature', pattern: 'layers/*/**', capture: ['feature'] }
    ]
  },
  plugins: { boundaries },
  rules: {
    'boundaries/dependencies': [
      2,
      {
        default: 'disallow',
        // Only classified→classified edges are governed. The root app/, server/,
        // and nuxt.config.ts are unclassified — neither restricted nor targets.
        policies: [
          // A feature may import ONLY its own tree, plus shared. Never another
          // feature — that cross-feature coupling is what this blocks.
          {
            from: { element: { type: 'feature' } },
            allow: [
              { element: { type: 'feature', captured: { feature: '{{from.captured.feature}}' } } },
              { element: { type: 'shared' } }
            ]
          },
          // shared: the reusable kernel. May lean on other shared only — never
          // reach up into a feature layer.
          { from: { element: { type: 'shared' } }, allow: [{ element: { type: 'shared' } }] }
        ]
      }
    ]
  }
}
